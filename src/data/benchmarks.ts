export function getBenchmarkResult(datasetId: string, modelId: string): BenchmarkResult | undefined {
  return benchmarkResults.find(
    r => r.datasetId === datasetId && r.modelId === modelId
  );
}

export function getDatasetBenchmarks(datasetId: string): BenchmarkResult[] {
  return benchmarkResults.filter(r => r.datasetId === datasetId);
}

export function getModelBenchmarks(modelId: string): BenchmarkResult[] {
  return benchmarkResults.filter(r => r.modelId === modelId);
}

export function getMetricValues(datasetId: string, metricId: string): Array<{ modelId: string; value: number }> {
  return benchmarkResults
    .filter(r => r.datasetId === datasetId && r.metrics[metricId] !== null && r.metrics[metricId] !== undefined)
    .map(r => ({
      modelId: r.modelId,
      value: r.metrics[metricId] as number,
    }));
}
